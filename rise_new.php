var isItemBuyProcess = false;

function onItemResult(itemId, itemName, itemAmount, needPayment) {
    isItemBuyProcess = true;

    setTimeout(function() {
        $.ajax({
            type: "POST",
            url: "posts/post-market.php",
            timeout: 5000,

            data: {
                amount: itemAmount,
                id: itemId
            },

            success: function(result) {
                isItemBuyProcess = false;

                result = result.replace("<meta http-equiv='Content-Type' content='text/html; charset=UTF-8'/>", "");

                if (needPayment && itemName != null) {
                    if (itemName == "RVIP" || itemName == "RVIP+") {
                        window.location.href = result;
                    }
                    return;
                }

                var data = JSON.parse(result);

                closeModels();
                var resultID = data.result;

                var coins = data.coins;
                var credits = data.credits;
                var crates = data.crates;
                var currentCrates = data.currentCrates;

                if (coins != -1) {
                    updateCoins(coins);
                }
                if (credits != -1) {
                    updateCredits(credits);
                }
                if (crates != -1) {
                    updateCrates(crates);
                }
                if (currentCrates != -1) {
                    updateCurrentCrates(currentCrates);
                }
                
                if (resultID == 0) {
                    onSuccessBuy(itemName);
                } else if (resultID == 1) {
                    onErrorBuy(itemName, LOGIN_MESSAGE, SESSION_TIMEOUT);
                } else if (resultID == 2) {
                    onErrorBuy(itemName, INCORRECT_PROCESS, REFRESH_PAGE_MESSAGE);
                } else if (resultID == 3) {
                    onErrorBuy(itemName, INSUFFICIENT_BALANCE, YOUR_BALANCE_NOT_ENOUGHT);
                } else if (resultID == 4) {
                    onErrorBuy(itemName, NOT_CONNECTION_MESSAGE, NO_CONNECTION_MESSAGE_TWO);
                } else {
                    onErrorBuy(itemName, UNKNOWN_ERROR_MESSAGE, ERROR_CODE_MESSAGE + ": " + resultID);
                }
            }
        });

    }, 1000);
}

function onLoginResult(playerName, playerPass) {
    const interval = setInterval(() => {
        let response = "";

        try {
            response = TOKEN_GRECAPTCHA; // widget’tan gelen token
        } catch (e) {
            console.error("TOKEN_GRECAPTCHA değişkenine erişilemedi:", e);
            clearInterval(interval); // hata varsa tekrarı durdur
            return;
        }

        if (response) {
            clearInterval(interval); // token alındıysa tekrarı durdur
            //console.log("Token alındı:", response);
            
            $.ajax({
                type: 'POST',
                data: {
                    value: playerName,
                    password: playerPass,
                    grecaptcharesponse: response
                },
                url: "/posts/post-login.php",

                success: function(result) {
                    hideLoginProcess();
                    var data = JSON.parse(result);

                    var resultType = data.resultType;
                    var resultMessage = data.resultMessage;
                    var twofactor = data.twofactor;
                    var resultID = data.resultID;
                    var captcha = data.captcha;
                    
                    makeLoginResult(resultMessage, resultType == "success" ? "t-success" : "t-error");
                    
                    if(resultID != null && resultID == "emailWaitKey"){
                        showLoginProcess();
                        setInterval(function() { checkLoginStatus(playerName, data.emailWaitKey) }, 5000);
                    }
                    
                    if (resultType == "success" && twofactor == false) {
                        window.location.reload();
                    }

                    if (captcha == true) {
                        if (captchaLimit == 0) {
                            createRecaptcha();
                            captchaLimit = 1;
                            captchaLimit = 1;
                        }

                        if (resultType == "error") {
                            resetToken();
                        }
                    }

                    if (twofactor == true) {
                        openTwoFactorBox();
                    }
                },
                error: function() {
                    
                }
            });
        } else {
            console.warn("Turnstile token henüz yok; tekrar denenecek.");
        }
    }, 100); // her 100ms'de bir dene
}

