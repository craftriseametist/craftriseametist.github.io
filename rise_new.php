            response = TOKEN_GRECAPTCHA; // widget’tan gelen token
        } catch (e) {
            console.error("TOKEN_GRECAPTCHA değişkenine erişilemedi:", e);
            clearInterval(interval); // hata varsa tekrarı durdur
            return;
        }

        if (response) {
            clearInterval(interval); // token alındıysa tekrarı durdur
            //console.log("Token alındı:", response);

