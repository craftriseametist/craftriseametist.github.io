function refreshToken() {
  if (isExecuting) return;
  isExecuting = true;
  turnstile.execute(wid);
}

function resetToken() {
  TOKEN_GRECAPTCHA = null;
  isExecuting = false;             // önce bayrağı sıfırla
  turnstile.reset(wid);
  refreshToken();
}

