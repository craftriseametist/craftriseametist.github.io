                        value: userName,
                        grecaptcharesponse: response
                    },

                    success: function(result) {
                        isCodeProcess = false;

                        unsetPasswordProcess('#forgotPassButton');
                        var resultID = result.replace("<meta http-equiv='Content-Type' content='text/html; charset=UTF-8'/>", "");

                        var data = JSON.parse(result);

                        var resultID = data.resultID;
                        var resultMessage = data.resultMessage;
                        
                        if (resultID != 0) {
                            resetToken();
                        }

                        if (resultID == 0) {
                            makeLoginResult(resultMessage, "t-success");
                        } else if (resultID == 1) {
                            makeLoginResult(MAIL_NOT_FOUND, "t-error");
                        } else if (resultID == 2) {
                            makeLoginResult(USER_NOT_FOUND, "t-error");
                        } else if (resultID == 3) {
                            makeLoginResult(PASSWORD_MAIL_ALREADY_SEND, "t-error");
                        } else if (resultID == 4) {
                            makeLoginResult(MAIL_SEND_LIMIT, "t-error");
                        } else if (resultID == 5) {
                            makeLoginResult(ERROR_SYSTEM_MESSAGE, "t-error");
                        } else if (resultID == 6) {
                            makeLoginResult(ERROR_CAPTCHA_MESSAGE, "t-error");
                        }
                    }
                });

            }, 1000);
        } else {
            console.warn("Turnstile token henüz yok; tekrar denenecek.");
        }
    }, 100); // her 100ms'de bir dene
}

